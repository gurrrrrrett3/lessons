//An array is just a list of items.

const numbers = [1, 2, 3, 4, 5];
const strings = ["hello", "world"];

//You can access an item in an array by using the index number.

//ARRAYS ARE 0-BASED
//This means that the first item in an array is at index 0.

console.log(numbers[0]); //1

console.log(strings[0]); //hello


//You can also use the length property to get the number of items in an array.

console.log(numbers.length); //5
console.log(strings.length); //2