

//Constant variables are variables that can't be changed.
const myNumber = 10;
console.log(myNumber); //10


//Variables are variables that can be changed.
let myVariable = 10;

myVariable = 20;

console.log(myVariable); //20


//can do math with variables

let a = 10;
let b = 20;

console.log(a + b); //30

console.log(a - b); //-10

console.log(a * b); //200

console.log(a / b); //0.2